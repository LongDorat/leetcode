namespace Template;

public class Solution
{
    /// <summary>
    /// An example method that doubles the input value.
    /// </summary>
    /// <param name="input">An integer input value.</param>
    /// <returns>The doubled value of the input.</returns>
    /// <remarks> Time Complexity: O(1), Space Complexity: O(1) </remarks>
    public int ExampleMethod(int input)
    {
        return input * 2;
    }
}
